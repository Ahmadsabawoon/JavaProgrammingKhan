package day09_scanner_pracrice;

public class RemainderOperator {
    public static void main(String[] args) {
        System.out.println(10 / 3); // 3 we have focus on the front number , that how many time using to complete the target.
        System.out.println(10 /5); // 2
        System.out.println(10 % 3); // 1 3+3+3+1
        System.out.println(10 % 5); //  0 5+5+0

        System.out.println(20 / 7); //2
        System.out.println(20 % 7); // 6  its remainder and could make it the other 7.
    }
}
