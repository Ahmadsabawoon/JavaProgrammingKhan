package day12_conditional_statements;

public class PracticeByMySelf {
    public static void main(String[] args){
        int restaurantRating = 5;
        switch(restaurantRating) {
            case 1:
                System.out.println("This is not my favourite restaurant");
                break;
            case 2:
                System.out.println("This is good restaurant");
                break;
            case 3:
                System.out.println("This  restaurant is fantastic ");
                break;
            default:
                System.out.println("I have never dined at this restaurant");


        }






















































    }

}














