package day04_variables_intro;

public class VarialbelNamingRules {
    public static void main(String[] args){

        int  public2 = 5;
        System.out.println(public2);

        int static2 = 22;
        int _static = 22;
        int $static = 44;
        int staticVar = 234;

        int salary$ = 55;
        // int 1stMonthSalary = 3000; -> error, cannot start with number
        int $ = 10;
        int _ = 3;
        System.out.println($);
        System.out.println(_);
        // Above variable work fine, but not recommended.

        // int number-of-friends = 400;    dash is not allowed and error
        int numberOfFriends = 401;
        int number_of_friends= 401; // not convention









    }
}
